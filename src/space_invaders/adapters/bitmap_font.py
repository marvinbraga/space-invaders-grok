class BitmapFont:
    """Renders uppercase text onto pygame surfaces via pixel glyphs."""

    def __init__(self, pixel_size: int = 2, letter_spacing: int = 1) -> None:
        if pixel_size < 1:
            msg = "pixel_size must be >= 1"
            raise ValueError(msg)
        self._px = pixel_size
        self._gap = letter_spacing

    @property
    def line_height(self) -> int:
        return 7 * self._px

    def measure(self, text: str) -> tuple[int, int]:
        if not text:
            return 0, self.line_height
        width = 0
        for _ch in text.upper():
            width += 5 * self._px + self._gap
        return max(0, width - self._gap), self.line_height

    def render(self, text: str, color: tuple[int, int, int]) -> pygame.Surface:
        return _render_cached(text.upper(), color, self._px, self._gap)


@lru_cache(maxsize=512)
def _render_cached(
    text: str,
    color: tuple[int, int, int],
    px: int,
    gap: int,
) -> pygame.Surface:
    if not text:
        return pygame.Surface((1, 7 * px), pygame.SRCALPHA)
    char_w = 5 * px
    width = len(text) * (char_w + gap) - gap
    height = 7 * px
    surface = pygame.Surface((max(1, width), height), pygame.SRCALPHA)
    x = 0
    for ch in text:
        glyph = _GLYPHS.get(ch, _GLYPHS["?"])
        for row, bits in enumerate(glyph):
            for col, bit in enumerate(bits):
                if bit != "1":
                    continue
                surface.fill(
                    color,
                    pygame.Rect(x + col * px, row * px, px, px),
                )
        x += char_w + gap
    return surface
